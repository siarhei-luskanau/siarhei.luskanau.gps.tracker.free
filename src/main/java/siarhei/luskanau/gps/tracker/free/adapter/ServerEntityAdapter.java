package siarhei.luskanau.gps.tracker.free.adapter;

import android.content.Context;
import android.view.LayoutInflater;
import android.view.View;
import android.view.ViewGroup;
import android.widget.ArrayAdapter;

import siarhei.luskanau.gps.tracker.free.R;
import siarhei.luskanau.gps.tracker.free.settings.ServerEntity;

public class ServerEntityAdapter extends ArrayAdapter<ServerEntity> {

    private LayoutInflater layoutInflater;

    public ServerEntityAdapter(Context context) {
        super(context, 0);
        layoutInflater = LayoutInflater.from(context);
    }

    @Override
    public View getView(int position, View convertView, ViewGroup parent) {
        View view = convertView;
        if (view == null) {
            view = layoutInflater.inflate(R.layout.list_item_server, parent, false);
        }
        ServerEntity serverEntity = getItem(position);
        return view;
    }

}
